<p><b>Creating a Portfolio Listing Page</b></p>

<p>There are three different type of portfolio</p>

<p>Ajax portfolio, Full background portfolio and Standard portfolio</p>

<p><br></p>

<p><b>Please do the following to create an ajax or full background portfolio:</b></p>

<p><br></p>

<ol>
<li>Please navigate to Pages =&gt; Add New</li>
<li>Locate the "fancy portfolio" option panel bellow the text editor.</li>
<li>Choose ajax or full background portfolio and mark the categories of portfolio items you would like to show.</li>
<li>Publish</li>
</ol>
<p><b><br></b></p>

<p><b>Please do the following to create a standard portfolio: </b></p>

<p><br></p>

<p></p>

<ol>
<li>
<span style="font-size: 13px; font-style: normal;">Please navigate to Pages =&gt; Add New</span><br>
</li>
