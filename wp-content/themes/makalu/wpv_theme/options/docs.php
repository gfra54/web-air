<p><b>Setting up Blog Listing Page</b><b><br></b></p>

<p><b><br></b></p>

<ol>
<li>Please make sure that you haven't chosen any page as a Posts page in Settings =&gt; Reading of your WP Admin Dashboard</li>
<li>Please navigate to Pages =&gt; Add New</li>
<li>In the VamTam Drag &amp; Drop Page Builder &nbsp;click on the "Add New Element" button in the top right corner and choose Blog. Choose your setting from the Shortcode editor and then click on "Save Element" button.</li>
<li>Publish.</li>
</ol>
<p><br></p>

<p>In VamTam Theme Options =&gt; Style =&gt; Posts you will find options to hide the meta info and image re-sizing options.</p>

<p>Last but not least you have many setting that control your blog listing in the shortcode itself. Number of the posts per page, pagination on/off, image on/off , showing particular category or a post are the one found there.</p>

<p><br></p>

<p><b>Blog Archive Pages</b> : Categories, Post formats, Tags, Monthly Archives. These blog listings are created automatically. In&nbsp;VamTam Theme Options =&gt; Layout&nbsp; - you can change the default layout and respectively the layout of the blog archive pages.</p>

<p><br></p>

<p><b>Setting up Blog Posts</b></p>

<p>You have a variety of post types you can create with our themes.&nbsp;</p>

<p><br></p>

<p><span style="background-color: rgb(215, 227, 188);">Please make sure you select the right post type. If you don’t – you’ll be wondering why the theme isn’t working for you.</span></p>

<p><br></p>

<p>Featured Image:</p>

<p style="text-align: justify;"><span style="background-color: rgb(215, 227, 188);">Our themes, use the image you set as the Featured Image on top of your post and also as the main blog page preview image –&nbsp;</span></p>

<p style="text-align: justify;"><span style="background-color: rgb(215, 227, 188);">technically known as the thumbnail. To set the Feature Image, go to the last box in the bottom of the right sidebar on your post&nbsp;</span></p>

<p style="text-align: justify;"><span style="background-color: rgb(215, 227, 188);">option panel and select Use As Featured Image on your Image Upload editor. Naturally, if you want another image beneath the&nbsp;</span></p>

<p style="text-align: justify;"><span style="background-color: rgb(215, 227, 188);">main image somewhere in your article, this you will want to insert the standard way.</span></p>

<p><br></p>

<p><b>Creating Blog Post</b></p>

<p><br></p>

<ol>
<li>Please navigate to Posts =&gt; Add New</li>
<li>Please insert featured image - the native WP functionality on the right hand side. This is the image that will appear in the blog listing and the post itself.</li>
<li>Please insert the content you need into the text editor.</li>
<li>Choose the category, tags or post format from the options on the right hand side.</li>
<li>Choose the post format settings in the options just bellow the text editor if you have chosen post format different from standard.</li>
<li>Publish.</li>
</ol>
<p><br></p>

<p>You can customize the layout and style of your Blog Post in two locations:</p>

<p><br></p>

<ol>
<li>If you need to change the layout - please navigate to VamTam theme options - layout and its sub-menus -body/footer, if you need the change for particular area of the website.<br>
If you need to change the style (colors, backgrounds, typography) please navigate to theme options - styles and to its sub menus, if you would like to style particular area of the website.<br>
&nbsp;<br>
In theme options - style - posts you will find options to hide the meta info and image resizing options.<br>
&nbsp;</li>
<li>You have an option to override some of global options . These options is located just bellow your text editor in pages/posts/portfolios. You can create unique style and layout for any page/post/portfolio post.<br>
&nbsp;<br>
&nbsp;</li>
</ol>
<p><b>Setting up Portfolio Listing Page and Portfolio Posts/Items</b></p>

