<p><span style="font-size: 24px;">Setting up a slider</span></p>

<p><span style="font-size: 18px;"><br></span></p>

<p>We always recommend importing the demo content. If you have done so you would have imported the sample sliders that come with the theme and replicate the one on the demo website. You can use these sliders as a starting point for your own website. The "sample sliders" are well designed and complex sliders. If you are not yet familiar with the slider and theme options it is a must to use them.<br><br>
The theme comes with 2 different sliders -&nbsp;<b>VamTam Slider</b>&nbsp;and&nbsp;<b>Layered Slider.</b>&nbsp;<br>
VamTam slider is easy to set up with simple animations. The animations are pre set and it is difficult to alter them. It comes with fade/slide/zoom in and multi image type animations.<br>
Layered slider is powerful and complex parallax slider. It gives you unlimited opportunities to create slides with many sub layers with different animation.<br><br><span style="background-color: rgb(215, 227, 188);">Please note that the Layered Slider is a plugin and have its own option panel found in the main left side navigation menu - Layered Slider WP</span><br></p>

